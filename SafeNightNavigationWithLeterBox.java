	static void dfs(int[][] grid, int x, int y, int m,
	int n, Map<Integer, Integer> map,
	boolean isBoxFound, boolean[] row, boolean[] col, int len)
	{
		if(!(x<m && x>=0 && y<n && y>=0 && grid[x][y] > 0 && (row[x] || col[y])))
		   return;
	   
		if(x==m-1 && y==n-1)
		{
			int boxPath = map.getOrDefault(len+1, 0);
			
			if(isBoxFound || grid[x][y] == 5)// the last cell might contain the letter box(hence check it)
				map.put(len+1, boxPath+1);
			
            return;			
		}
					
		if(grid[x][y] == 5)
			isBoxFound = true;
		
		int temp = grid[x][y];
		grid[x][y] = -1;
		
		dfs(grid, x-1, y, m, n, map, isBoxFound, row, col, len+1);
		dfs(grid, x, y-1, m, n, map, isBoxFound, row, col, len+1);
		dfs(grid, x+1, y, m, n, map, isBoxFound, row, col, len+1);
		dfs(grid, x, y+1, m, n, map, isBoxFound, row, col, len+1);
		
		grid[x][y] = temp;		
	}
	
	
